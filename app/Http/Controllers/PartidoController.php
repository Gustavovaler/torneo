     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
       $partido = new Partido();
       $partido->resultado_jug_2 = $request->input('resultado_jug_2');
       $partido->jugador_2 = $request->input('jugador_2');
       $partido->resultado_jug_1 = $request->input('resultado_jug_1');
       $partido->jugador_1 = $request->input('jugador_1');
       $partido->save();
       return redirect('partidos/create');
    }

